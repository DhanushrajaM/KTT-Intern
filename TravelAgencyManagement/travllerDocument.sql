    Traveller_id INT,
    documentType VARCHAR(30),
    documentNumber VARCHAR(50),
    issueCountry VARCHAR(50),
    issueDate DATE,
    expiryDate DATE,
    verificationStatus DEFAULT 'Pending',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_traveller_document FOREIGN KEY (Traveller_id) REFERENCES Travellers(id)
);
