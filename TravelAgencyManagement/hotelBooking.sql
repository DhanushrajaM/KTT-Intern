CREATE TABLE IF NOT EXISTS HotelBookings(
    id SERIAL PRIMARY KEY,
    Booking_id INT NOT NULL,
    Hotel_id INt NOT NULL,
    check_in DATE NOT NULL,
    check_out DATE NOT NULL,
    price_per_night DECIMAL(10,2) NOT NULL,
    room_type VARCHAR(50),
    room_count INT CHECK(room_count>0),
    guest_count INT CHECK(guest_count>0),
    price DECIMAL(10,2),
    total_amount DECIMAL(10,2),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_hotelBooking_booking FOREIGN KEY (Booking_id) REFERENCES Bookings(id),
    CONSTRAINT fk_hotelBooking_hotel FOREIGN KEY (Hotel_id) REFERENCES Hotels(id)
);

