CREATE TABLE IF NOT EXISTS BookingVehicles(
    id SERIAL PRIMARY KEY,
    Booking_id INT NOT NULL,
    Vehicle_id INT NOT NULL,
    Driver_id INT NOT NULL,
    pickup_location VARCHAR(50),
    drop_location VARCHAR(50),
    pickup_time TIMESTAMP,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_bookingVehicle_booking FOREIGN KEY (Booking_id) REFERENCES Bookings(id),
    CONSTRAINT fk_bookingVehicle_vehicle FOREIGN KEY (Vehicle_id) REFERENCES Vehicles(id),
    CONSTRAINT fk_bookingVehicle_driver FOREIGN KEY (Driver_id) REFERENCES Drivers(id)
);

